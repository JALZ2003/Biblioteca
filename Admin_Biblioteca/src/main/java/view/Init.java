    public void setTable(List<Object[]> info_libros) {
        DefaultTableModel modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return false;
            }
        };
        modelo.addColumn("Codigo del libro");
        modelo.addColumn("Titulo");
        modelo.addColumn("Autor");
        modelo.addColumn("Genero");
        modelo.addColumn("Estado libro");

        for (int i = 0; i < info_libros.size(); i++) {
            modelo.addRow(info_libros.get(i));
            modelo.isCellEditable(0, i);
        }
        jTable1.setModel(modelo);
    }

    public void paintStatus() {
        JTableDesing desing = new JTableDesing(Color.BLUE, "DISPO");
        jTable1.getColumnModel().getColumn(0).setCellRenderer(desing);
        jTable1.getColumnModel().getColumn(4).setCellRenderer(desing);
    }

    public void updateTable() {
        Info_libros_controller info_libros_Controller = new Info_libros_controller();
        List<Object[]> detalleVenta = info_libros_Controller.getInfoLibros();
        setTable(detalleVenta);
    }

